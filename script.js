function hideAllScreens() {
    document.querySelectorAll(".screen")
        .forEach(s => s.classList.add("hidden"));
}


let currentCategory = null;

window.openCategory = function(cat) {

    currentCategory = cat;

    usedItems[cat] = new Set();

    hideAllScreens();

    if (cat === "feedback") {

        document
            .getElementById("feedback")
            .classList.remove("hidden");

        return;
    }

    const catScreen =
        document.getElementById("category");

    catScreen.classList.remove("hidden");

    catScreen.style.backgroundImage =
        `url(${backgrounds[cat]})`;

    newItem();
};

window.newItem = function() {
    const list = data[currentCategory];

    if (usedItems[currentCategory].size === list.length) {
        usedItems[currentCategory] = new Set();
    }

    let item;

    do {
        item = list[Math.floor(Math.random() * list.length)];
    } while (usedItems[currentCategory].has(item));

    usedItems[currentCategory].add(item);

    let htmlText = item
        .replace(/\n/g, "<br>")
        .replace(/Never have I ever\.\.\./g, "<strong><em>Never Have I Ever...<br></em></strong>")
        .replace(/This or That\?/g, "<strong><em>This or That?<br></em></strong>")
        .replace(/'/g,"&quot;")
    document.getElementById("cardText").innerHTML = htmlText;
};


window.sendFeedback = function() {

    const message =
        document.getElementById("feedbackText").value;

    const status =
        document.getElementById("status");

    if (!message.trim()) {
        status.innerText = "Please enter feedback!!";
        return;
    }

    emailjs.send(
        "service_gee5esf",
        "template_79siiqp",
        { message: message }
    )
        .then(() => {
            status.innerText = "✅ Feedback sent!";
            document.getElementById("feedbackText").value = "";
        })
        .catch(() => {
            status.innerText = "❌ Error, sending failed!";
        });
};


window.goHome = function() {
    hideAllScreens();
    document.getElementById("home")
        .classList.remove("hidden");
};




